    public void showAllInMemoryTable(Map<String,String> data){
        Object[][] tagArray = firstScan.mapToArrayConverter(data);
        String[] columnName = {jTable1.getColumnName(0),jTable1.getColumnName(1)};
        DefaultTableModel model = new DefaultTableModel(tagArray,columnName);
        jTable1.setModel(model);
    }
    
    public void showTagsInTable(Map<String,String> tags){
        /*
        Bu method firstScan sınıfındaki mapToArrayConverter methoduyla
        HashMap türündeki etiket ve adreslerini diziye çevirdikten sonra
        etiketleri etiket tablosuna yazar.
        */
        Object[][] tagArray = firstScan.mapToArrayConverter(tags);
        
        String[] columnName = {jTable3.getColumnName(0),jTable3.getColumnName(1)};
        
        DefaultTableModel model = new DefaultTableModel(tagArray,columnName);
        jTable3.setModel(model);
    }
    
