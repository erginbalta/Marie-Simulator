    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Program Yükle Butonu
        for(String line : jTextArea1.getText().split("\\n")){
            line = line.toUpperCase();
            codeBlocks.add(line);
        }
        List<String> codeBlock = firstScan.codeSeperator(codeBlocks);
        Map<String,String> tags = firstScan.tagCatcher(codeBlock);
        showTagsInTable(tags);
        Map<String,String> memoryMap = firstScan.addressInserter(codeBlock);
        showAllInMemoryTable(memoryMap);
    }//GEN-LAST:event_jButton3ActionPerformed

